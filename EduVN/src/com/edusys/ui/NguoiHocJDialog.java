    NguoiHocDAO dao = new NguoiHocDAO();
    int row = -1;

    public void init() {
        fillTable();
        row = -1;
        updateStatus();
        this.getContentPane().setBackground(Color.pink);
        JTableHeader header = tblNguoiHoc.getTableHeader();
        header.setFont(new Font("segoe", Font.BOLD, 15));
        header.setForeground(new Color(51, 51, 51));
        jScrollPane1.setBorder(BorderFactory.createEmptyBorder());
        jScrollPane2.setBorder(BorderFactory.createEmptyBorder());
        lblHinh.setIcon(ResizeImage("users\\default.png"));
    }

    public void fillTable() {
        DefaultTableModel model = (DefaultTableModel) tblNguoiHoc.getModel();
        model.setRowCount(0);
        try {
            String keyword = txtTimKiem.getText();
            List<NguoiHoc> list = dao.selectByKeyword(keyword);
            for (NguoiHoc nh : list) {
                Object[] row = {
                    nh.getMaNH(),
                    nh.getHoTen(),
                    nh.getGioiTinh() ? "Nam" : "Nữ",
                    XDate.toString(nh.getNgaySinh(), "MM/dd/yyyy"),
                    nh.getDienThoai(),
                    nh.getEmail(),
                    nh.getMaNV(),
                    XDate.toString(nh.getNgayDK(), "MM/dd/yyyy")
                };
                model.addRow(row);
            }
        } catch (Exception e) {
            MsgBox.alert(this, "Lỗi truy vấn dữ liệu!");
        }
    }

    public void insert() {
        if (isValidated()) {
            NguoiHoc model = getForm();
            try {
                dao.insert(model);
                fillTable();
                clearForm();
                MsgBox.alert(this, "Thêm mới thành công!");
            } catch (Exception e) {
                MsgBox.alert(this, e.getMessage());
            }
        }
    }

    public void update() {
        if (isValidated()) {
            NguoiHoc model = getForm();
            try {
                dao.update(model);
                fillTable();
                MsgBox.alert(this, "Cập nhật thành công!");
            } catch (Exception e) {
                MsgBox.alert(this, "Cập nhật thất bại!");
            }
        }
    }

    public void delete() {
        if (!Auth.isManager()) {
            MsgBox.alert(this, "Bạn không có quyền xóa người học!");
        } else if (MsgBox.confirm(this, "Bạn thực sự muốn xóa người học này?")) {
            String manh = txtMaNH.getText();
            try {
                dao.delete(manh);
                fillTable();
                clearForm();
                MsgBox.alert(this, "Xóa thành công!");
            } catch (Exception e) {
                MsgBox.alert(this, "Xóa thất bại!");
            }
        }
    }

    public void clearForm() {
        setForm(new NguoiHoc());
        row = -1;
        updateStatus();
    }

    public void edit() {
        String manh = (String) tblNguoiHoc.getValueAt(row, 0);
        NguoiHoc nh = dao.selectById(manh);
        setForm(nh);
        updateStatus();
        tabs.setSelectedIndex(0);
    }

    public void setForm(NguoiHoc nh) {
        txtMaNH.setText(nh.getMaNH());
        txtHoTen.setText(nh.getHoTen());
        if (nh.getGioiTinh()) {
            rdoNam.setSelected(true);
        } else {
            rdoNu.setSelected(true);
        }
        txtNgaySinh.setText(XDate.toString(nh.getNgaySinh(), "MM/dd/yyyy"));
        txtDienThoai.setText(nh.getDienThoai());
        txtEmail.setText(nh.getEmail());
        txtGhiChu.setText(nh.getGhiChu());
        if (nh.getHinh()!= null) {
            lblHinh.setToolTipText(nh.getHinh());
            lblHinh.setIcon(ResizeImage("users//" + nh.getHinh()));
        } else {
            lblHinh.setIcon(ResizeImage("users\\default.png"));
            lblHinh.setToolTipText(null);
        }        
    }

    public NguoiHoc getForm() {
        NguoiHoc nh = new NguoiHoc();
        nh.setMaNH(txtMaNH.getText());
        nh.setHoTen(txtHoTen.getText());
        nh.setGioiTinh(rdoNam.isSelected());
        try {
            nh.setNgaySinh(XDate.toDate(txtNgaySinh.getText(), "MM/dd/yyyy"));
        } catch (Exception e) {
            MsgBox.alert(this, "Vui lòng nhập đúng định dạng ngày sinh");
            return null;
        }

        nh.setDienThoai(txtDienThoai.getText());
        nh.setEmail(txtEmail.getText());
        nh.setGhiChu(txtGhiChu.getText());
        nh.setMaNV(Auth.user.getMaNV());
        nh.setNgayDK(new Date());
        nh.setHinh(lblHinh.getToolTipText());
        return nh;
    }

    public void first() {
        row = 0;
        edit();
    }

    public void prev() {
        if (row > 0) {
            row--;
            edit();
        }
    }

    public void next() {
        if (row < tblNguoiHoc.getRowCount() - 1) {
            row++;
            edit();
        }
    }

    public void last() {
        row = tblNguoiHoc.getRowCount() - 1;
        edit();
    }

    public void updateStatus() {
        boolean edit = (row >= 0);
        boolean first = (row == 0);
        boolean last = (row == tblNguoiHoc.getRowCount() - 1);
        // Trạng thái form
        txtMaNH.setEditable(!edit);
        btnThem.setEnabled(!edit);
        btnSua.setEnabled(edit);
        btnXoa.setEnabled(edit);

        // Trạng thái điều hướng
        btnFirst.setEnabled(edit && !first);
        btnPrev.setEnabled(edit && !first);
        btnNext.setEnabled(edit && !last);
        btnLast.setEnabled(edit && !last);
    }

    private void timKiem() {
        fillTable();
        clearForm();
        row = -1;
        updateStatus();
    }

    public boolean isValidated() {
        NguoiHoc nh = getForm();
        if (nh == null) {
            return false;
        }
        if (nh.getMaNH().length() == 0) {
            MsgBox.alert(this, "Không để trống mã người học!");
        } else if (nh.getHoTen().length() == 0) {
            MsgBox.alert(this, "Không để trống họ và tên người học!");
        } else if (!rdoNam.isSelected() && !rdoNu.isSelected()) {
            MsgBox.alert(this, "Vui lòng chọn giới tính!");
        } else if (nh.getNgaySinh().toString().length() == 0) {
            MsgBox.alert(this, "Không để ngày sinh người học!");
        } else if (nh.getEmail().length() == 0 || !checkMail(nh.getEmail())) {
            MsgBox.alert(this, "Vui lòng nhập Email của người học!");
        } else if (nh.getDienThoai().length() < 10
                || nh.getDienThoai().length() > 12
                || !checkSDT(nh.getDienThoai())) {
            MsgBox.alert(this, "Vui lòng nhập số điện thoại 10-12 số!");
        } else if (lblHinh.getToolTipText() == null) {
            MsgBox.alert(this, "Vui lòng chọn hình đại diện");
        } else {
            return true;
        }
        return false;
    }

    public boolean checkSDT(String sdt) {
        String regex = "^[0-9]+$";

        if (!sdt.matches(regex)) {
            return false;
        }
        return true;
    }

    public boolean checkMail(String mail) {
        String regex = "^[a-zA-Z0-9]{1}[a-zA-Z0-9\\.]*[^\\.]@{0}([a-zA-Z0-9]+\\.+[a-zA-Z0-9]+)+$";

        if (!mail.matches(regex)) {
            return false;
        }
        return true;
    }
    
    public void chonAnh() {
        JFileChooser fileChooser = new JFileChooser();
        if (fileChooser.showOpenDialog(this) == JFileChooser.APPROVE_OPTION) {
            File file = fileChooser.getSelectedFile();
            XImage.save(file, "users"); // lưu hình vào thư mục logos
            String pathHinh = file.getAbsolutePath();
            ImageIcon icon = XImage.read(file.getName()); // đọc hình từ logos
            lblHinh.setIcon(ResizeImage(pathHinh));
            lblHinh.setToolTipText(file.getName()); // giữ tên hình trong tooltip
        }
    }
    
    public ImageIcon ResizeImage(String ImagePath) {
        ImageIcon MyImage = new ImageIcon(ImagePath);
        Image img = MyImage.getImage();
        Image newImg = img.getScaledInstance(lblHinh.getWidth(), lblHinh.getHeight(), Image.SCALE_SMOOTH);
        ImageIcon image = new ImageIcon(newImg);
        return image;
    }

